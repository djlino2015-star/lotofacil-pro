// ─── ANÁLISE ──────────────────────────────────────────────────
function analyze(games) {
  const total = games.length;
  if (!total) return null;
  const freq = {}, lastSeen = {}, pairFreq = {};
  for (let n = 1; n <= 25; n++) { freq[n] = 0; lastSeen[n] = total; }

  games.forEach((g, idx) => {
    g.numeros.forEach(n => { freq[n]++; if (lastSeen[n] === total) lastSeen[n] = idx; });
    for (let i = 0; i < g.numeros.length; i++)
      for (let j = i + 1; j < g.numeros.length; j++) {
        const k = `${g.numeros[i]}-${g.numeros[j]}`;
        pairFreq[k] = (pairFreq[k] || 0) + 1;
      }
  });

  const last50 = games.slice(0, 50), prev50 = games.slice(50, 100);
  const f50 = {}, fp = {};
  for (let n = 1; n <= 25; n++) { f50[n] = 0; fp[n] = 0; }
  last50.forEach(g => g.numeros.forEach(n => f50[n]++));
  prev50.forEach(g => g.numeros.forEach(n => fp[n]++));
  const tendencia = {};
  for (let n = 1; n <= 25; n++) tendencia[n] = f50[n] - fp[n];

  // Par/Ímpar por jogo
  const parImpar = games.map(g => {
    const pares = g.numeros.filter(n => n % 2 === 0).length;
    return { concurso: g.concurso, pares, impares: 15 - pares };
  });
  const avgPares = parImpar.reduce((s, x) => s + x.pares, 0) / total;

  // Sequências (números consecutivos)
  const seqStats = games.map(g => {
    let maxSeq = 1, cur = 1;
    const sorted = [...g.numeros].sort((a, b) => a - b);
    for (let i = 1; i < sorted.length; i++) {
      if (sorted[i] === sorted[i - 1] + 1) { cur++; maxSeq = Math.max(maxSeq, cur); }
      else cur = 1;
    }
    return maxSeq;
  });
  const avgSeq = seqStats.reduce((s, x) => s + x, 0) / total;
  const seqDist = [0, 0, 0, 0, 0, 0];
  seqStats.forEach(s => { if (s <= 5) seqDist[s - 1]++; });

  // Distribuição quadrante (1-5, 6-10, 11-15, 16-20, 21-25)
  const quadDist = games.map(g => {
    const q = [0, 0, 0, 0, 0];
    g.numeros.forEach(n => q[Math.floor((n - 1) / 5)]++);
    return q;
  });
  const avgQuad = [0, 0, 0, 0, 0];
  quadDist.forEach(q => q.forEach((v, i) => avgQuad[i] += v / total));

  const scores = {};
  for (let n = 1; n <= 25; n++) {
    scores[n] = (freq[n] / total) * 0.5 + (tendencia[n] / 50) * 0.3 + (lastSeen[n] < 10 ? 0.3 : lastSeen[n] < 25 ? 0.1 : -0.1) * 0.2;
  }
  const suggested = Object.entries(scores).sort((a, b) => b[1] - a[1]).slice(0, 15).map(([n]) => parseInt(n)).sort((a, b) => a - b);
  const topPairs = Object.entries(pairFreq).sort((a, b) => b[1] - a[1]).slice(0, 20);

  return { freq, lastSeen, tendencia, topPairs, total, suggested, avgPares, avgSeq, seqDist, avgQuad, parImpar, seqStats };
}

